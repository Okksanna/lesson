#Ви маєте три числа, вони вводяться з консолі. Перше число називається fizz, друге називається buzz.
#До третього необхідно дорахувати від одиниці. Дивлячись на поточне число,
#якщо воно кратне fizz – треба виводити F замість числа. #
#Якщо число кратне buzz – треба виводити B замість числа.
#Якщо число кратне і fizz, і buzz, треба виводити FB. Якщо воно не кратне нічому, виводимо число.
#І так - поки не буде досягнуто третього введеного числа.

#Приклад умови та результату: Введено числа 2, 5, 18
#Висновок має бути таким: 1 F 3 F B F 7 F 9 FB 11 F 13 F B F 17 F
fizz=int(input('fizz='))
buzz=int(input('buzz='))
thizz=int(input('thizz='))
if buzz<fizz:
    print('Error')
else:
    rez=''
    for d in range(1,thizz+1):
        if not(d%fizz):
            rez+='F'
            if not(d%buzz):
                rez+='B'
        elif not(d%buzz):
            rez+='B'
        else:
            rez+=str(d)
        rez+=' '
    
    
    print(rez)
            
    